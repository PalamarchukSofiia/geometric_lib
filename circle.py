# Импортируем модуль для использования числа П
import math

# Функция для вычисления площади круга
def area(r):
    # Возвращаем площадь круга, вычисленную по формуле: pi * r^2
    return math.pi * r * r

# Функция для вычисления периметра круга
def perimeter(r):
    # Возвращаем периметр круга, вычисленную по формуле: 2 * pi * r
    return 2 * math.pi * r

