# Функция для вычисления площади квадрата
def area(a):
    # Возвращаем площадь квадрата, вычисленную по формуле: сторона * сторона
    return a * a

# Функция для вычисления периметра квадрата
def perimeter(a):
    # Возвращаем периметр квадрата, вычисленную по формуле: 4 * сторона
    return 4 * a
