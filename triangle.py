# Функция для вычисления площади треугольника по формуле Герона
def area(a, b, c):
    # Сначала вычисляем полупериметр треугольника
    s = (a + b + c) / 2
    # Почему-то возвращаем только полупериметр, не продолжая вычислять площадь?
    return s

# Функция для вычисления периметра треугольника
def perimeter(a, b, c):
    # Возвращаем периметр треугольника, вычисленный по формуле: a + b + c
    return a + b + c
